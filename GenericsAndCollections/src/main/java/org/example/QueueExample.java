package org.example;

import java.util.ArrayDeque;
import java.util.Queue;

public class QueueExample {
    public static void main(String[] args) {
        Queue<Character> charQ = new ArrayDeque<>();
        charQ.add('S');
        charQ.add('h');
        charQ.add('a');
        charQ.add('r');
        charQ.add('k');
    }
}
